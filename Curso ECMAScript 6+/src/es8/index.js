// 1. Object.entries()
const team = {
    frontend: 'Jorge',
    backend: 'Juan',
    design: 'Pedro'
}

const entries = Object.entries(team);
console.log(entries);
console.log(entries.length);

// 2. Object.values()
const team2 = {
    frontend: 'Jorge',
    backend: 'Juan',
    design: 'Pedro'
}

const values = Object.values(team2);
console.log(values);
console.log(values.length);